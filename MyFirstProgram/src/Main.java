public class Main {

    public static void main(String[] args) {

        // this is a comment

        System.out.print("\tI love pizza!\n");

        System.out.println("It\"s really good!"); // works as print with \n at the end

        System.out.println("It's really tasty!");

        /*

        this is a
        multiline
        comment

         */

        System.out.println();  // shortcut: sout + tab

    }

}
